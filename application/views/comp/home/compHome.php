                    <p> There's a few reasons I like this place.</p>
                    <p>1)  Hayes valley  is usually pretty packed and on certain nights, it's really hard to find restaurants when you don't have reservations.  Dobbs Ferry is one of those where you'll wait, but the wait's actually tolerable (~1 hour, compared to say like Suppenkuche who told us we didn't stand a chance)
                        2)  They have a bar next door (same owner) so you can drink while waiting for a table and they will text you to let you know when your turn comes.  My friend from out of town came in to visit and we managed to spend a good deal of time catching up at the bar.
                        3)  Their food is good.  I've tried their pizza, pasta, steak, and short rib and they all taste great.  My friend's boyfriend also ordered the fried pumpkin (appetizer) which I would recommend.
                        4)  They have a pretty good atmosphere going.  The restaurant is quiet enough to have conversations and there is a skylight in the middle of the dining area (I find that really cool!)</p>
                    <p>This restaurant is very solid and hasn't disappointed me.  I can't give it five stars since I don't think their food is AMAZING, but I will definitely be back. </p>

